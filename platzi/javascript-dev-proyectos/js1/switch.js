var numero = 1;

switch (numero){
    case 1:
        console.log("Soy un uno");
        break;
    case 10:
        console.log("Uy, soy un 10");
        break;
    case 100:
        console.log("Ya me pasé, soy un 100");
        break;
    default:
        console.log("¿Yo? no soy lo que esperabas.")
}